def _baseline_retry_risk(report: dict[str, Any]) -> str:
    signals = report["evidence_bundle"].get("signals", {})
    failure_type = report["diagnosis"]["failure_type"]
    if failure_type in {"fault_localization_failure", "context_drift_or_repeated_mistake"}:
        return "high: likely to repeat the same misleading context"
    if failure_type in {"environment_or_tool_failure", "validation_target_failure"}:
        return "high: retrying repair before validation wastes budget"
    if signals.get("patch_touches_tests") and not signals.get("patch_touches_source"):
        return "medium: retry may preserve an unsafe patch direction"
    return "unknown: needs more evidence"


def _markdown_cell(value: object) -> str:
    text = str(value).replace("\n", " ").replace("|", "\\|")
    return " ".join(text.split())
